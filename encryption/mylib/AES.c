void isb(uint8 x[][4])
{
	for(uint8 i=0; i<4; ++i)
		for(uint8 j=0; j<4; ++j)
			x[i][j] = iSB[x[i][j]];
}

void isr(uint8 x[][4])
{
	lshift(x[1][3], x[1][2], x[1][1], x[1][0]);
	swap(x[2][0], x[2][2]);
	swap(x[2][1], x[2][3]);
	lshift(x[3][0], x[3][1], x[3][2], x[3][3]);
}

uint8 mul(uint8 x, uint8 y)
{
	uint8 ret = 0;
	for(uint8 tmp; y; y>>=1)
	{
		if(y & 1) ret ^= x;
		tmp = x << 1;
		if(x >= 128) tmp ^= 0x1b;
		x = tmp;
	}
	return ret;
}

const uint8 ia[][4] =
{
    {0xe,0xb,0xd,0x9},
    {0x9,0xe,0xb,0xd},
    {0xd,0x9,0xe,0xb},
    {0xb,0xd,0x9,0xe}
};

void imc(uint8 x[][4])
{
	uint8 rt[4][4];
	memset(rt, 0, sizeof rt);
	for(uint8 j=0; j<4; ++j)
		for(uint8 i=0; i<4; ++i)
			for(uint8 k=0; k<4; ++k)
				rt[i][j] ^= mul(ia[i][k], x[k][j]);
	for(uint8 i=0; i<4; ++i)
		for(uint8 j=0; j<4; ++j)
			x[i][j] = rt[i][j];
}

void iark(uint8 x[][4], uint8 **k)
{
	uint8 (*p)[4] = (uint8(*)[])(*k);
	for(uint8 i=0; i<4; ++i)
		for(uint8 j=0; j<4; ++j)
			x[i][j] ^= p[j][i];
	(*k) -= 16;
}

void decrypt(uint8 s[16], uint8 k[])
{
	uint8 (*m)[4] = (uint8(*)[])s;
	uint8 *p = k + (NR<<4);
//	ke(k);
	iark(m, &p);
	isr(m);
	isb(m);
	for(uint8 i=NR-1; i>0; --i)
		iark(m, &p),
		imc(m),
		isr(m),
		isb(m);
	iark(m, &p);
}

