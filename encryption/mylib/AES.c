void ke(uint8 k[])
{
	uint8 *p = k + 16, *l = k, *r = k + 12;
	for(uint8 i=4; i<44; ++i)
		if(i & 3)
		{
			*p++ = *l++ ^ *r++;
			*p++ = *l++ ^ *r++;
			*p++ = *l++ ^ *r++;
			*p++ = *l++ ^ *r++;
		}
		else
		{
			*p++ = *l++;
			*p++ = *l++;
			*p++ = *l++;
			*p = *l++; p -= 3;
			lshift(*p, p[1], p[2], p[3]);
			*p = SB[*p]; ++p;
			*p = SB[*p]; ++p;
			*p = SB[*p]; ++p;
			*p = SB[*p]; p-=3;
			*p ^= RC[i>>2];
			*p++ ^= *r++;
			*p++ ^= *r++;
			*p++ ^= *r++;
			*p++ ^= *r++;
		}
}

#define NR (10)

void encrypt(uint8 s[16], uint8 k[])
{
	uint8 (*m)[4] = (uint8(*)[])s;
	uint8 *p = k;
//	ke(k);
	ark(m, &p);
	for(uint8 i=1; i<NR; ++i)
		sb(m),
		sr(m),
		mc(m),
		ark(m, &p);
	sb(m);
	sr(m);
	ark(m, &p);
}

