void sb(uint8 x[][4])
{
	for(uint8 i=0; i<4; ++i)
		for(uint8 j=0; j<4; ++j)
			x[i][j] = SB[x[i][j]];
}

void sr(uint8 x[][4])
{
#define swap(x, y) do{uint8 t=(x); (x)=(y); (y)=t;} while(0) 
#define lshift(a, b, c, d) do{uint8 t=(a); (a)=(b); (b)=(c); (c)=(d); (d)=t;} while(0)
	lshift(x[1][0], x[1][1], x[1][2], x[1][3]);
	swap(x[2][0], x[2][2]);
	swap(x[2][1], x[2][3]);
	lshift(x[3][3], x[3][2], x[3][1], x[3][0]);
}

const uint8 a[][4] =
{
    {2,3,1,1},
    {1,2,3,1},
    {1,1,2,3},
    {3,1,1,2}
};

void mc(uint8 x[][4])
{
	uint8 rt[4][4];
	memset(rt, 0, sizeof rt);
	for(uint8 j=0; j<4; ++j)
		for(uint8 i=0; i<4; ++i)
			for(uint8 k=0; k<4; ++k)
			{
				uint8 t = x[k][j];
				if(a[i][k] & 1)
					rt[i][j] ^= t;
				if(a[i][k] & 2)
				{
					rt[i][j] ^= t << 1;
					if(t & 0x80)
						rt[i][j] ^= 0x1B;
				}
			}
	for(uint8 i=0; i<4; ++i)
		for(uint8 j=0; j<4; ++j)
			x[i][j] = rt[i][j];
}

void ark(uint8 x[][4], uint8 **k)
{
	uint8 (*p)[4] = (uint8(*)[])(*k);
	for(uint8 i=0; i<4; ++i)
		for(uint8 j=0; j<4; ++j)
			x[i][j] ^= p[j][i];
	(*k) += 16;
}

